const toTaiwanChinese = OpenCC.Converter({ from: "cn", to: "tw" });

function deepConvert(value, converter, useTaiwanOverrides = false) {
  if (typeof value === "string") {
    const source = useTaiwanOverrides ? applyTaiwanOverrides(value) : value;
    const converted = converter(source);
    return useTaiwanOverrides ? applyTaiwanOverrides(converted) : converted;
  }
  if (Array.isArray(value)) return value.map((item) => deepConvert(item, converter, useTaiwanOverrides));
  if (!value || typeof value !== "object") return value;
  return Object.fromEntries(Object.entries(value).map(([key, item]) => [key, deepConvert(item, converter, useTaiwanOverrides)]));
}

function applyTaiwanOverrides(text) {
  let output = text;
  const entries = Object.entries(TAIWAN_OVERRIDES).sort((a, b) => b[0].length - a[0].length);
  for (const [source, target] of entries) {
    const traditionalSource = toTaiwanChinese(source);
    output = output.split(source).join(target);
    if (traditionalSource !== source) output = output.split(traditionalSource).join(target);
  }
  return output;
}

function createCatalog(options = {}) {
  const locale = normalizeLocale(options.locale || "cn");
  const zhTW = locale === "tw";
  const converter = zhTW ? toTaiwanChinese : (value) => value;
  const lang = zhTW ? "zh-TW" : "zh-CN";
  const twRootMessages = deepConvert(ROOT_MESSAGES, toTaiwanChinese, true);
  const twPublicMessages = deepConvert(PUBLIC_MESSAGES, toTaiwanChinese, true);
  const twPhrases = deepConvert(PHRASES, toTaiwanChinese, true);
  const twDynamicPhrases = deepConvert(DYNAMIC_PHRASES, toTaiwanChinese, true);
  const twLocalizableStrings = deepConvert(LOCALIZABLE_STRINGS, toTaiwanChinese, true);
  const twAllPhrases = deepConvert(ALL_PHRASES, toTaiwanChinese, true);
  const twCompoundPhrases = deepConvert(COMPOUND_PHRASES, toTaiwanChinese, true);
  const twPatternPhrases = deepConvert(PATTERN_PHRASES, toTaiwanChinese, true);
  const rootMessages = deepConvert(ROOT_MESSAGES, converter, zhTW);
  const publicMessages = deepConvert(PUBLIC_MESSAGES, converter, zhTW);
  const phrases = deepConvert(PHRASES, converter, zhTW);
  const dynamicPhrases = deepConvert(DYNAMIC_PHRASES, converter, zhTW);
  const localizableStrings = deepConvert(LOCALIZABLE_STRINGS, converter, zhTW);
  const allPhrases = deepConvert(ALL_PHRASES, converter, zhTW);
  const compoundPhrases = deepConvert(COMPOUND_PHRASES, converter, zhTW);
  const patternPhrases = deepConvert(PATTERN_PHRASES, converter, zhTW);
  return {
    locale,
    lang,
    rootMessages,
    publicMessages,
    phrases: withAlternateSourceValues(phrases, PHRASES, twPhrases),
    dynamicPhrases: withAlternateSourceValues(dynamicPhrases, DYNAMIC_PHRASES, twDynamicPhrases),
    localizableStrings,
    allPhrases: withAlternateSourceValues(allPhrases, ALL_PHRASES, twAllPhrases),
    compoundPhrases: withAlternateSourceValues(compoundPhrases, COMPOUND_PHRASES, twCompoundPhrases),
    patternPhrases,
    sourceAlternates: {
      rootMessages: withAlternateSourceValues(rootMessages, ROOT_MESSAGES, twRootMessages),
      publicMessages: withAlternateSourceValues(publicMessages, PUBLIC_MESSAGES, twPublicMessages),
      localizableStrings: withAlternateSourceValues(localizableStrings, LOCALIZABLE_STRINGS, twLocalizableStrings),
      patternPhrases: withAlternateSourceValues(patternPhrases, PATTERN_PHRASES, twPatternPhrases)
    },
    metaDescription: deepConvert(META_DESCRIPTION, converter, zhTW)
  };
}

function withAlternateSourceValues(target, simplifiedSource, traditionalSource) {
  const output = { ...target };
  for (const key of Object.keys(target)) {
    const targetValue = target[key];
    if (typeof targetValue !== "string") continue;
    const simplifiedValue = simplifiedSource[key];
    const traditionalValue = traditionalSource[key];
    if (typeof simplifiedValue === "string") output[simplifiedValue] = targetValue;
    if (typeof traditionalValue === "string") output[traditionalValue] = targetValue;
  }
  return output;
}

function normalizeLocale(locale) {
  const value = String(locale || "cn").toLowerCase();
  if (["cn", "zh", "zh-cn", "zh_cn", "simplified", "simplified-chinese"].includes(value)) return "cn";
  if (["tw", "zh-tw", "zh_tw", "traditional", "traditional-chinese", "taiwan"].includes(value)) return "tw";
  throw new Error(`未知语言：${locale}`);
}

